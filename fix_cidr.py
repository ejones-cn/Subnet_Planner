# 修复CIDR正则表达式的脚本
with open('f:/trae_projects/Subnet_Planner/windows_app.py', 'r', encoding='utf-8') as f:
    content = f.read()

# 定义旧的和新的正则表达式模式
old_pattern = """
        # CIDR格式验证正则表达式，支持IPv4和IPv6
        # 简化的CIDR验证正则表达式，支持IPv4和IPv6
        self.cidr_pattern = (
            # IPv4 CIDR 格式：例如 192.168.1.0/24
            r'^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}'  # noqa: W605
            + r'(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)/(?:[0-9]|[1-2][0-9]|3[0-2])$'
            + r'|'
            # IPv6 CIDR 格式：例如 2001:0db8::/32 或 2001:0db8:85a3:0000:0000:8a2e:0370:7334/64
            + r'^(?=(?:[0-9a-fA-F]{0,4}:){2,7}[0-9a-fA-F]{0,4}$)'
            + r'(?:[0-9a-fA-F]{1,4}:){7}[0-9a-fA-F]{1,4}/(?:[0-9]|[1-9][0-9]|1[01][0-9]|12[0-8])$'
            + r'|'
            r'^(?=(?:[0-9a-fA-F]{0,4}:){2,7}:$)'
            + r'(?:[0-9a-fA-F]{1,4}:){6}:(?:[0-9a-fA-F]{1,4})?/(?:[0-9]|[1-9][0-9]|1[01][0-9]|12[0-8])$'
            + r'|'
            r'^(?=(?:[0-9a-fA-F]{0,4}:){1,6}::(?:[0-9a-fA-F]{0,4}:){0,6}[0-9a-fA-F]{0,4}$)'
            + r'(?:[0-9a-fA-F]{1,4}:){1,6}::(?:[0-9a-fA-F]{1,4}:){0,6}[0-9a-fA-F]{1,4}/(?:[0-9]|[1-9][0-9]|1[01][0-9]|12[0-8])$'
        )
"""

new_pattern = """
        # CIDR格式验证正则表达式，支持IPv4和IPv6
        self.cidr_pattern = (
            # IPv4 CIDR 格式：例如 192.168.1.0/24
            r'^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}'  # noqa: W605
            + r'(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)/(?:[0-9]|[1-2][0-9]|3[0-2])$'
            + r'|'
            # IPv6 CIDR 格式：支持各种格式，包括简化的2001:0db8::/32
            r'^[0-9a-fA-F:.]+/(?:[0-9]|[1-9][0-9]|1[01][0-9]|12[0-8])$'
        )
"""

# 替换内容
new_content = content.replace(old_pattern, new_pattern)

# 写回文件
with open('f:/trae_projects/Subnet_Planner/windows_app.py', 'w', encoding='utf-8') as f:
    f.write(new_content)

print("CIDR正则表达式已修复")
