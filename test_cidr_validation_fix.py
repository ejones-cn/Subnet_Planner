#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import re

# 修复前的CIDR正则表达式（有问题）
old_cidr_pattern = (
    r'^((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?).){3}'
    + r'(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)/'
    + r'([0-9]|[1-2][0-9]|3[0-2])$'
)

# 修复后的CIDR正则表达式
new_cidr_pattern = (
    r'^((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}'
    + r'(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)/'
    + r'([0-9]|[1-2][0-9]|3[0-2])$'
)

# 测试用例
test_cases = [
    "10.21.48.0/4",       # 有效CIDR
    "10.21.48。0/4",      # 无效CIDR（中文句号）
    "192.168.1、3/24",    # 无效CIDR（中文顿号）
    "192.168.1.256/24",   # 无效CIDR（八位组超过255）
    "192.168.1/24",       # 无效CIDR（缺少八位组）
    "192.168.1.1.1/24",   # 无效CIDR（多了一个八位组）
    "192.168.1.1/33",     # 无效CIDR（CIDR值超过32）
]

print("=== 测试CIDR验证修复效果 ===\n")
print(f"{'测试用例':<20} {'修复前结果':<15} {'修复后结果':<15} {'预期结果':<15}")
print("-" * 65)

for test_case in test_cases:
    # 修复前的匹配结果
    old_match = bool(re.match(old_cidr_pattern, test_case))
    
    # 修复后的匹配结果
    new_match = bool(re.match(new_cidr_pattern, test_case))
    
    # 预期结果
    expected = True if test_case == "10.21.48.0/4" else False
    
    # 输出结果
    print(f"{test_case:<20} {old_match!s:<15} {new_match!s:<15} {expected!s:<15}")

print("\n=== 修复说明 ===")
print("问题：修复前的CIDR正则表达式中点号(.)没有被转义，导致它匹配任何字符，包括中文句号(。)")
print("解决方案：将点号转义为\\.，确保它只匹配实际的点号字符")
print("效果：现在'10.21.48。0/4'这样的输入会被正确识别为无效CIDR，显示红色")